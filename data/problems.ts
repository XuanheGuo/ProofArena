export const problems: Problem[] = rawProblems.map((problem) => {
  const autoMatches = matchTagsToKnowledge(problem.tags);
  const allMatches = [...autoMatches, ...(problem.manualMatches ?? [])];

  return {
    ...problem,
    autoMatches,
    knowledgeIds: problem.knowledgeIds ?? [...new Set(allMatches.flatMap((match) => match.matchedKnowledgeIds))],
    insightIds: problem.insightIds ?? [...new Set(allMatches.flatMap((match) => match.matchedInsightIds))],
  };
});

export function getProblem(id: string) {
  return problems.find((problem) => problem.id === id);
}

export function getAverageScore(problem: Problem) {
  const values = problem.solutions.flatMap((item) => Object.values(item.scores));
  return values.reduce((sum, score) => sum + score, 0) / values.length;
}

export function getSolutionAverage(item: Problem["solutions"][number]) {
  const values = Object.values(item.scores);
  return values.reduce((sum, value) => sum + value, 0) / values.length;
}

export function getBestSolution(problem: Problem, score: keyof Problem["solutions"][number]["scores"]) {
  return [...problem.solutions].sort((a, b) => b.scores[score] - a.scores[score])[0];
}

export function getLearningIndex(problem: Problem) {
  const bestExplanation = Math.max(...problem.solutions.map((item) => item.scores.explanation));
  const bestExamReady = Math.max(...problem.solutions.map((item) => item.scores.examReady));
  const bestElegance = Math.max(...problem.solutions.map((item) => item.scores.elegance));
  return (bestExplanation * 0.5 + bestExamReady * 0.25 + bestElegance * 0.25).toFixed(1);
}
